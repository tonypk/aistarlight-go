// fieldRow draws a computation line: line number | description | amount box
func (b *birFormBuilder) fieldRow(lineNo, label, value string, bold bool) {
	cw := b.contentW()
	h := birBoxHeight
	lineNoW := 28.0
	amtW := 100.0
	labelW := cw - lineNoW - amtW - 4

	// Draw border
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	b.pdf.Rect(b.marginL, b.y, cw, h, "D")

	// Line number cell (left)
	b.pdf.Rect(b.marginL, b.y, lineNoW, h, "D")
	b.pdf.SetFont(birFontFamily, "B", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(lineNoW, h, lineNo, "", 0, "C", false, 0, "")

	// Label
	style := ""
	if bold {
		style = "B"
	}
	b.pdf.SetFont(birFontFamily, style, birSmallSize)
	b.pdf.SetXY(b.marginL+lineNoW+2, b.y+1)
	b.pdf.CellFormat(labelW, h-2, label, "", 0, "L", false, 0, "")

	// Amount box (right)
	amtX := b.marginL + cw - amtW
	b.pdf.Rect(amtX, b.y, amtW, h, "D")
	b.pdf.SetFont(birFontFamily, style, birFontSize)
	b.pdf.SetXY(amtX+2, b.y)
	b.pdf.CellFormat(amtW-4, h, formatAmount(value), "", 0, "R", false, 0, "")

	b.y += h
}

