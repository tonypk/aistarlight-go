// tinBoxes draws TIN number in separated digit boxes: [1][2][3]-[4][5][6]-[7][8][9]-[0][0][0]
func (b *birFormBuilder) tinBoxes(label, tin string) {
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL+4, b.y+2)
	b.pdf.CellFormat(30, birBoxHeight-4, label, "", 0, "L", false, 0, "")

	// Parse TIN: remove dashes, pad to 12 digits
	digits := strings.ReplaceAll(tin, "-", "")
	digits = strings.ReplaceAll(digits, " ", "")
	for len(digits) < 12 {
		digits += "0"
	}

	boxSize := birTINBoxSize
	startX := b.marginL + 38
	b.pdf.SetFont(birFontFamily, "B", birFontSize)
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)

	for i := 0; i < 12; i++ {
		x := startX + float64(i)*boxSize
		// Add dash separators at positions 3, 6, 9
		if i == 3 || i == 6 || i == 9 {
			b.pdf.SetFont(birFontFamily, "B", birFontSize+2)
			b.pdf.SetXY(x-2, b.y)
			b.pdf.CellFormat(6, birBoxHeight, "-", "", 0, "C", false, 0, "")
			startX += 4
			x = startX + float64(i)*boxSize
			b.pdf.SetFont(birFontFamily, "B", birFontSize)
		}

		// Draw box
		b.pdf.Rect(x, b.y, boxSize, birBoxHeight, "D")
		// Draw digit
		if i < len(digits) {
			b.pdf.SetXY(x, b.y)
			b.pdf.CellFormat(boxSize, birBoxHeight, string(digits[i]), "", 0, "C", false, 0, "")
		}
	}

	b.y += birBoxHeight + 2
}

