// gap adds vertical spacing.
func (b *birFormBuilder) gap(pts float64) {
	b.y += pts
}

// checkPageBreak adds a new page if remaining space is less than needed.
func (b *birFormBuilder) checkPageBreak(needed float64) {
	if b.y+needed > b.pageH-30 {
		b.addPage()
	}
}

