// textField draws a label + value with underline, like official form fill-in fields.
func (b *birFormBuilder) textField(label, value string, labelW float64) {
	cw := b.contentW()
	h := birBoxHeight

	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL+4, b.y+1)
	b.pdf.CellFormat(labelW, h-2, label, "", 0, "L", false, 0, "")

	// Value with underline
	valueX := b.marginL + 4 + labelW
	valueW := cw - labelW - 12
	b.pdf.SetFont(birFontFamily, "B", birFontSize)
	b.pdf.SetXY(valueX, b.y+1)
	b.pdf.CellFormat(valueW, h-2, value, "", 0, "L", false, 0, "")

	// Underline
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	b.pdf.Line(valueX, b.y+h-1, valueX+valueW, b.y+h-1)

	b.y += h + 1
}

// textFieldHalf draws two label+value pairs side by side.
func (b *birFormBuilder) textFieldHalf(label1, value1, label2, value2 string) {
	halfW := b.contentW() / 2
	h := birBoxHeight

	// Left field
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL+4, b.y+1)
	b.pdf.CellFormat(60, h-2, label1, "", 0, "L", false, 0, "")
	b.pdf.SetFont(birFontFamily, "B", birFontSize)
	b.pdf.SetXY(b.marginL+66, b.y+1)
	b.pdf.CellFormat(halfW-72, h-2, value1, "", 0, "L", false, 0, "")
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	b.pdf.Line(b.marginL+66, b.y+h-1, b.marginL+halfW-6, b.y+h-1)

	// Right field
	rightX := b.marginL + halfW
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetXY(rightX+4, b.y+1)
	b.pdf.CellFormat(60, h-2, label2, "", 0, "L", false, 0, "")
	b.pdf.SetFont(birFontFamily, "B", birFontSize)
	b.pdf.SetXY(rightX+66, b.y+1)
	b.pdf.CellFormat(halfW-72, h-2, value2, "", 0, "L", false, 0, "")
	b.pdf.Line(rightX+66, b.y+h-1, rightX+halfW-6, b.y+h-1)

	b.y += h + 1
}

