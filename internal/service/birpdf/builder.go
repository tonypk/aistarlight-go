// checkbox draws a checkbox with label: [X] Yes  [ ] No
func (b *birFormBuilder) checkbox(label string, checked bool) {
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL+4, b.y+1)
	b.pdf.CellFormat(100, 10, label, "", 0, "L", false, 0, "")

	x := b.marginL + 110

	// Yes checkbox
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	b.pdf.Rect(x, b.y+1, 8, 8, "D")
	if checked {
		b.pdf.SetFont(birFontFamily, "B", 7)
		b.pdf.SetXY(x, b.y+1)
		b.pdf.CellFormat(8, 8, "X", "", 0, "C", false, 0, "")
	}
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetXY(x+10, b.y+1)
	b.pdf.CellFormat(20, 8, "Yes", "", 0, "L", false, 0, "")

	// No checkbox
	x += 36
	b.pdf.Rect(x, b.y+1, 8, 8, "D")
	if !checked {
		b.pdf.SetFont(birFontFamily, "B", 7)
		b.pdf.SetXY(x, b.y+1)
		b.pdf.CellFormat(8, 8, "X", "", 0, "C", false, 0, "")
	}
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetXY(x+10, b.y+1)
	b.pdf.CellFormat(20, 8, "No", "", 0, "L", false, 0, "")

	b.y += 12
}

