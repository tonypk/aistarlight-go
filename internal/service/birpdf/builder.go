// formHeader draws the official BIR form header block.
// Republic of the Philippines / Department of Finance / Bureau of Internal Revenue
// BIR Form No. XXXX (large, centered)
// Title (centered)
func (b *birFormBuilder) formHeader(formNo, title, subtitle string) {
	cw := b.contentW()
	startY := b.y

	// Top border line
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(1.5)
	b.pdf.Line(b.marginL, startY, b.marginL+cw, startY)

	b.y += 4

	// "Republic of the Philippines"
	b.pdf.SetFont(birFontFamily, "", birSmallSize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(cw, 9, "Republic of the Philippines", "", 0, "C", false, 0, "")
	b.y += 9

	// "Department of Finance"
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(cw, 9, "Department of Finance", "", 0, "C", false, 0, "")
	b.y += 9

	// "Bureau of Internal Revenue"
	b.pdf.SetFont(birFontFamily, "B", birSmallSize+1)
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(cw, 9, "Bureau of Internal Revenue", "", 0, "C", false, 0, "")
	b.y += 12

	// Form number in large font
	b.pdf.SetFont(birFontFamily, "B", birTitleSize+2)
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(cw, 18, "BIR Form No. "+formNo, "", 0, "C", false, 0, "")
	b.y += 18

	// Form title
	b.pdf.SetFont(birFontFamily, "B", birFontSize+1)
	b.pdf.SetXY(b.marginL, b.y)
	b.pdf.CellFormat(cw, 12, title, "", 0, "C", false, 0, "")
	b.y += 12

	// Subtitle if present
	if subtitle != "" {
		b.pdf.SetFont(birFontFamily, "", birSmallSize)
		b.pdf.SetXY(b.marginL, b.y)
		b.pdf.CellFormat(cw, 9, subtitle, "", 0, "C", false, 0, "")
		b.y += 9
	}

	// Bottom border line
	b.y += 2
	b.pdf.SetLineWidth(1.5)
	b.pdf.Line(b.marginL, b.y, b.marginL+cw, b.y)
	b.y += 6
}

