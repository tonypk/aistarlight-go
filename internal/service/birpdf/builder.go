// GridCell represents one cell in a grid row.
type GridCell struct {
	Text    string
	Width   float64
	Align   string // "L", "C", "R"
	Bold    bool
	Fill    bool
	FillR   int
	FillG   int
	FillB   int
}

// gridHeaderRow draws a header row for schedule/grid tables.
func (b *birFormBuilder) gridHeaderRow(cells []GridCell) {
	h := birBoxHeight
	x := b.marginL

	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)

	for _, cell := range cells {
		if cell.Fill {
			b.pdf.SetFillColor(cell.FillR, cell.FillG, cell.FillB)
		} else {
			b.pdf.SetFillColor(220, 220, 220)
		}
		b.pdf.Rect(x, b.y, cell.Width, h, "FD")

		style := ""
		if cell.Bold {
			style = "B"
		}
		b.pdf.SetFont(birFontFamily, style, birTinySize)
		b.pdf.SetTextColor(0, 0, 0)
		b.pdf.SetXY(x+1, b.y+1)
		align := cell.Align
		if align == "" {
			align = "C"
		}
		b.pdf.CellFormat(cell.Width-2, h-2, cell.Text, "", 0, align, false, 0, "")
		x += cell.Width
	}
	b.y += h
}

// gridRow draws a data row in schedule/grid tables.
func (b *birFormBuilder) gridRow(cells []GridCell) {
	h := birBoxHeight
	x := b.marginL

	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)

	for _, cell := range cells {
		if cell.Fill {
			b.pdf.SetFillColor(cell.FillR, cell.FillG, cell.FillB)
			b.pdf.Rect(x, b.y, cell.Width, h, "FD")
		} else {
			b.pdf.Rect(x, b.y, cell.Width, h, "D")
		}

		style := ""
		if cell.Bold {
			style = "B"
		}
		b.pdf.SetFont(birFontFamily, style, birTinySize)
		b.pdf.SetTextColor(0, 0, 0)
		b.pdf.SetXY(x+1, b.y+1)
		align := cell.Align
		if align == "" {
			align = "L"
		}
		b.pdf.CellFormat(cell.Width-2, h-2, cell.Text, "", 0, align, false, 0, "")
		x += cell.Width
	}
	b.y += h
}

