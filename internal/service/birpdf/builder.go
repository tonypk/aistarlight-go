// signatureBlock draws the declaration and signature area.
func (b *birFormBuilder) signatureBlock(declarationText string) {
	cw := b.contentW()
	b.y += 4

	if declarationText == "" {
		declarationText = "I declare, under the penalties of perjury, that this return has been made in good faith, verified by me, and to the best of my knowledge and belief, is true, correct, and complete."
	}

	// Declaration text
	b.pdf.SetFont(birFontFamily, "", birTinySize)
	b.pdf.SetTextColor(0, 0, 0)
	b.pdf.SetXY(b.marginL+4, b.y)
	b.pdf.MultiCell(cw-8, 8, declarationText, "", "L", false)
	b.y += 20

	halfW := cw / 2

	// Left: Taxpayer/Authorized Representative
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	sigY := b.y + 20
	b.pdf.Line(b.marginL+10, sigY, b.marginL+halfW-20, sigY)
	b.pdf.SetFont(birFontFamily, "", birTinySize)
	b.pdf.SetXY(b.marginL+10, sigY+2)
	b.pdf.CellFormat(halfW-30, 8, "Taxpayer/Authorized Representative", "", 0, "C", false, 0, "")

	// Right: Date
	b.pdf.Line(b.marginL+halfW+20, sigY, b.marginL+cw-10, sigY)
	b.pdf.SetXY(b.marginL+halfW+20, sigY+2)
	b.pdf.CellFormat(halfW-30, 8, "Date", "", 0, "C", false, 0, "")

	b.y = sigY + 14

	// Title/Position
	sigY2 := b.y + 16
	b.pdf.Line(b.marginL+10, sigY2, b.marginL+halfW-20, sigY2)
	b.pdf.SetXY(b.marginL+10, sigY2+2)
	b.pdf.CellFormat(halfW-30, 8, "Title/Position", "", 0, "C", false, 0, "")

	// TIN
	b.pdf.Line(b.marginL+halfW+20, sigY2, b.marginL+cw-10, sigY2)
	b.pdf.SetXY(b.marginL+halfW+20, sigY2+2)
	b.pdf.CellFormat(halfW-30, 8, "TIN", "", 0, "C", false, 0, "")

	b.y = sigY2 + 14
}

