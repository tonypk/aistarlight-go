// partHeader draws "Part I - Background Information" style headers with black fill.
func (b *birFormBuilder) partHeader(text string) {
	cw := b.contentW()
	h := 16.0

	b.pdf.SetFillColor(0, 0, 0)
	b.pdf.Rect(b.marginL, b.y, cw, h, "F")

	b.pdf.SetTextColor(255, 255, 255)
	b.pdf.SetFont(birFontFamily, "B", birFontSize)
	b.pdf.SetXY(b.marginL+4, b.y+2)
	b.pdf.CellFormat(cw-8, h-4, text, "", 0, "L", false, 0, "")

	b.pdf.SetTextColor(0, 0, 0)
	b.y += h
}

