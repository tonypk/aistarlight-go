// BIR official form visual constants (extracted from official PDFs).
const (
	birFontFamily = "Arial"
	birFontSize   = 8.0
	birTitleSize  = 14.0
	birSmallSize  = 6.5
	birTinySize   = 5.5

	birLineWidth  = 0.4
	birBoxHeight  = 14.0
	birTINBoxSize = 10.0

	defaultMarginL = 36.0
	defaultMarginR = 36.0
	defaultMarginT = 28.0
)

// birFormBuilder provides official BIR form layout primitives.
type birFormBuilder struct {
	pdf     *fpdf.Fpdf
	y       float64
	pageW   float64
	pageH   float64
	marginL float64
	marginR float64
	formNo  string
	version string
	pageNum int
}

// PageSize represents a PDF page size in points.
type PageSize struct {
	W, H float64
}

// Common BIR form page sizes (in points).
var (
	SizeLegal     = PageSize{612, 1008} // 8.5 x 14 in
	SizeLetter    = PageSize{612, 792}  // 8.5 x 11 in
	SizeFolio     = PageSize{612, 936}  // 8.5 x 13 in
)

// newBuilder creates a new birFormBuilder with the given page size.
func newBuilder(size PageSize, formNo, version string) *birFormBuilder {
	pdf := fpdf.NewCustom(&fpdf.InitType{
		OrientationStr: "P",
		UnitStr:        "pt",
		Size:           fpdf.SizeType{Wd: size.W, Ht: size.H},
	})
	pdf.SetAutoPageBreak(false, 0)
	pdf.SetMargins(0, 0, 0)

	b := &birFormBuilder{
		pdf:     pdf,
		pageW:   size.W,
		pageH:   size.H,
		marginL: defaultMarginL,
		marginR: defaultMarginR,
		formNo:  formNo,
		version: version,
		pageNum: 0,
	}

	return b
}

// contentW returns the usable content width.
func (b *birFormBuilder) contentW() float64 {
	return b.pageW - b.marginL - b.marginR
}

// addPage adds a new page and draws the standard footer.
func (b *birFormBuilder) addPage() {
	b.pageNum++
	b.pdf.AddPage()
	b.y = defaultMarginT
}

// output writes the finished PDF to w.
func (b *birFormBuilder) output(w io.Writer) error {
	// Draw footers on all pages
	totalPages := b.pdf.PageCount()
	for i := 1; i <= totalPages; i++ {
		b.pdf.SetPage(i)
		b.drawPageFooter(i, totalPages)
	}
	return b.pdf.Output(w)
}

// drawPageFooter draws the footer on a specific page.
func (b *birFormBuilder) drawPageFooter(pageNo, totalPages int) {
	y := b.pageH - 20
	b.pdf.SetDrawColor(0, 0, 0)
	b.pdf.SetLineWidth(0.3)
	b.pdf.Line(b.marginL, y, b.pageW-b.marginR, y)

	b.pdf.SetFont(birFontFamily, "", birTinySize)
	b.pdf.SetTextColor(100, 100, 100)
	b.pdf.SetXY(b.marginL, y+2)
	b.pdf.CellFormat(b.contentW()/2, 10, b.formNo+" "+b.version, "", 0, "L", false, 0, "")
	b.pdf.SetXY(b.marginL+b.contentW()/2, y+2)
	b.pdf.CellFormat(b.contentW()/2, 10,
		fmt.Sprintf("Page %d of %d", pageNo, totalPages), "", 0, "R", false, 0, "")

	b.pdf.SetFont(birFontFamily, "", birTinySize-1)
	b.pdf.SetTextColor(130, 130, 130)
	b.pdf.SetXY(b.marginL, y+10)
	b.pdf.CellFormat(b.contentW(), 8,
		"Generated by AIStarlight - Draft for review purposes only, not for official filing",
		"", 0, "C", false, 0, "")
}

